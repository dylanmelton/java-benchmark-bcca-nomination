package app;

public class NominationForm {
    String nominatorName;
    String nominatorEmail;
    String schoolOrAffiliation;
    String position;
    String relationToNominee;
    String studentName;
    Int studentAge;
    String studentContact;
    String graduationDate;
    String aptitudeQuestion;
    String perseveranceQuestion;
    String dedicationQuestion;
    String workEthicQuestion;
    String extraInfo;

    public NominationForm(String nominatorName, String nominatorEmail, String schoolOrAffiliation, String position,
            String relationToNominee, String studentName, Int studentAge, String studentContact, String graduationDate,
            String aptitudeQuestion, String perseveranceQuestion, String dedicationQuestion, String workEthicQuestion,
            String extraInfo) {
        this.nominatorInfo = nominatoInfo;
        this.nominatorName = nominatorName;
        this.nominatorEmail = nominatorEmail;
        this.schoolOrAffiliation = schoolOrAffiliation;
        this.position = position;
        this.relationToNominee = relationToNominee;
        this.studentName = studentName;
        this.studentAge = studentAge;
        this.studentContact = studentContact;
        this.graduationDate = graduationDate;
        this.aptitudeQuestion = aptitudeQuestion;
        this.perseveranceQuestion = perseveranceQuestion;
        this.dedicationQuestion = dedicationQuestion;
        this.workEthicQuestion = workEthicQuestion;
        this.extraInfo = extraInfo;
    }

}